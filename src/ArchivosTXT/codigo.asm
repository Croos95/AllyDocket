.MODEL SMALL
.STACK 100h
.DATA
output DB  'Reporte Salida ' , 10,13  
        DB  '***************' , 10,13  
        DB  'Resultado:     ' , 10,13  
        DB  '***************' , 10,13  
resultado_mod DW 0
num DW 29
i DW 0
A DW 10
B DW 10
T0 DW ?
.CODE
START:
MOV AX, @DATA
MOV DS, AX
MOV ES, AX
MOV SI, OFFSET output
PRINT_LOOP:
MOV AH, 05h
LODSB
CMP AL, 0
JE FIN_PRINT
MOV DL, AL
INT 21h
JMP PRINT_LOOP
FIN_PRINT:
MOV AX, 29
MOV num, AX
MOV AX, 0
MOV i, AX
MOV AX, 10
MOV A, AX
MOV AX, 10
MOV B, AX
MOV AX, 0
MOV resultado_mod, AX
MOV AX, i
CMP AX, #A
JGE GOTO Label1
MOV AX, A
MOV BX, B
MUL BX
MOV T0, AX
MOV AX, T0
MOV i, AX
MOV AX, i
CALL PRINT_NUM
Label1:
MOV AX, 4C00h
INT 21h
PRINT_NUM:
PUSH AX
PUSH BX
PUSH CX
PUSH DX
MOV BX, 10
MOV CX, 0
MOV DX, 0
DIVIDE:
XOR DX, DX
DIV BX
PUSH DX
INC CX
OR AX, AX
JNZ DIVIDE
PRINT_LOOP_NUM:
POP DX
ADD DL, 30h
MOV AH, 05h
INT 21h
LOOP PRINT_LOOP_NUM
MOV DL, 0Ah
MOV AH, 02h
INT 21h
MOV DL, 0Dh
MOV AH, 02h
INT 21h
POP DX
POP CX
POP BX
POP AX
RET
PRINT_TEXT:
MOV AH, 05h
LEA DX, [SI]
INT 21h
RET
END START
