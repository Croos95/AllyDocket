.MODEL SMALL
.STACK 100h
.DATA
AX DW 12
.CODE
START:
MOV AX, @DATA
MOV DS, AX
MOV ES, AX
MOV AX, 12
MOV AX, AX
MOV AX, AX
CALL PRINT_NUM
MOV AX, 4C00h
INT 21h
PRINT_NUM:
PUSH AX
PUSH BX
PUSH CX
PUSH DX
MOV BX, 10
MOV CX, 0
MOV DX, 0
DIVIDE:
XOR DX, DX
DIV BX
PUSH DX
INC CX
OR AX, AX
JNZ DIVIDE
PRINT_LOOP:
POP DX
ADD DL, 30h
MOV AH, 05h
INT 21h
LOOP PRINT_LOOP
POP DX
POP CX
POP BX
POP AX
RET
PRINT_TEXT:
MOV AH, 05h
INT 21h
RET
END START
